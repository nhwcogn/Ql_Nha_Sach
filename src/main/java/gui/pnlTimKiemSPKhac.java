    private void napDLVaoTextField(){
        int n = table.getSelectedRow();
        if(n == -1)
            return;
        ArrayList<SanPham> listSanPham = new ArrayList<>();
        listSanPham = DAO.timDanhSachSanPhamTheoMaSP(model.getValueAt(n, 0).toString());
        for(SanPham sp: listSanPham){
            txtMaSP.setText(model.getValueAt(n, 0).toString());
            
            txtTenSP.setText(sp.getTenSanPham());
            cbxLoaiSP.addItem(sp.getLoaiSanPham().getLoaiSP());
            cbxLoaiSP.setSelectedItem(sp.getLoaiSanPham().getLoaiSP());
        }
    }
