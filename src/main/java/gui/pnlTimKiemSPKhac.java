    private void btnTimSPKhacActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimSPKhacActionPerformed
        // TODO add your handling code here:
        ArrayList<SanPham> list = new ArrayList<>();
        if(!txtMaSP.getText().equals("") && !txtMaSP.getText().equals("")){
            list = DAO.timDanhSachSanPhamTheoMaSP(txtMaSP.getText());
            ArrayList<SanPham> temp = new ArrayList<>();
            for(SanPham sp: list){
                if(sp.getTenSanPham().toLowerCase().contains(txtMaSP.getText().toLowerCase()))
                    temp.add(sp);
            }
            clearTable();
            loadTableTuDanhSach(list);
            
        }
        else if(!txtTenSP.getText().equals("")){
            list = DAO.timDanhSachSanPhamTheoTenSP(txtTenSP.getText());
            clearTable();
            loadTableTuDanhSach(list);
        }
        else
            JOptionPane.showMessageDialog(null, "Chưa nhập thông tin");
    }//GEN-LAST:event_btnTimSPKhacActionPerformed

