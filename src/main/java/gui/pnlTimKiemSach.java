    public void loadTable(){
        list = sachDAO.dsSach();
        
        for( Sach s: list){
            //System.out.println(s);
            model.addRow(new Object[]{
                s.getSanPham().getMa_SanPham(), s.getSanPham().getTenSanPham(),
                s.getTacGia().getTenTacGia(),
                s.getNamSanXuat(), s.getNhaXuatBan().getTenNXB()
            });
        }
    }
    public void loadTableTuDanhSach(ArrayList<Sach> list){
        for( Sach s: list){
            //System.out.println(s);
            model.addRow(new Object[]{
                s.getSanPham().getMa_SanPham(), s.getSanPham().getTenSanPham(),
                s.getTacGia().getTenTacGia(),
                s.getNamSanXuat(), s.getNhaXuatBan().getTenNXB()
            });
        }
    }
    
