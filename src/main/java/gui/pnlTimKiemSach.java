    public void napDLVaoTextfield(){
        int n = table.getSelectedRow();
        if(n == -1)
            return;
        ArrayList<Sach> list = new ArrayList<>();
        list = sachDAO.dsSachTheoMa(model.getValueAt(n, 0).toString());
        for(Sach s: list){
            txtMaSach.setText(table.getValueAt(n, 0).toString());
            txtTenSach.setText(s.getSanPham().getTenSanPham());
            cmbNhaXB.addItem(s.getNhaXuatBan().getTenNXB());
            cmbNhaXB.setSelectedItem(s.getNhaXuatBan().getTenNXB());
            
            txtTacGia.setText(s.getTacGia().getTenTacGia());
            txtNamXB.setText(String.valueOf(s.getNamSanXuat()));
        } 
    }
    
